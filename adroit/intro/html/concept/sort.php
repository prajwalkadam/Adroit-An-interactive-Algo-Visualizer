                      <div class="" data-aos="fade-up" data-aos-delay="200"></div>
           <h3 class="mb-4" data-aos="fade-up" data-aos-delay="300" style="color: darkblue;"> Bubble Sort</h3>
                      <h5 class="mb-4" data-aos="fade-up" data-aos-delay="300">Bubble Sort is a simple algorithm which is used to sort a given set of n elements provided in form of an array with n number of elements. Bubble Sort compares all the element one by one and sort them based on their values.
If we have total n elements, then we need to repeat this process for n-1 times.
<br>
Following are the steps involved in bubble sort(for sorting a given array in ascending order):
<br>
1. Starting with the first element(index = 0), compare the current element with the next element of the array.<br>
2. If the current element is greater than the next element of the array, swap them.<br>
3. If the current element is less than the next element, move to the next element. Repeat Step 1.</h5>
                      <h3 class="mb-4" data-aos="fade-up" data-aos-delay="300" style="color: darkblue;"> Selection Sort</h3>
                      <h5 class="mb-4" data-aos="fade-up" data-aos-delay="300">The selection sort algorithm sorts an array by repeatedly finding the minimum element (considering ascending order) from unsorted part and putting it at the beginning. The algorithm maintains two subarrays in a given array.<br>
1) The subarray which is already sorted. <br>
2) Remaining subarray which is unsorted.<br>
In every iteration of selection sort, the minimum element (considering ascending order) from the unsorted subarray is picked and moved to the sorted subarray. 
